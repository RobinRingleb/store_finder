public class Branches {
	
	private String name;
	private String url;
	private double latitude;
	private double longitude;
	
	public String getName() {
		return name;
	}
	
	public String getUrl() {
		return url;
	}
	
	public double getLatitude() {
		return latitude;
	}
	
	public double getLongitude() {
		return longitude;
	}
}
